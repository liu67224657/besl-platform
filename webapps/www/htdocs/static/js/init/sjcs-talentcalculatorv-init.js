    function initAddTalentTotal() {
        for (var key in talentObj.addTalent) {
            var talentInfo = talentConfig[talentObj.tclass].talent[key];
            var consume = talentInfo['consume'];
            for (var i = 0; i < consume.length; i++) {
                var consumeObj = consume[i];
                if (talentObj.addTalent[key] <= consumeObj.level) {
                    if (i == 0) {
                        talentObj.total += talentObj.addTalent[key] * consumeObj.consume;
                    } else {
                        talentObj.total += (talentObj.addTalent[key] - consume[i - 1].level) * consumeObj.consume;
                    }
                    break;
                } else {
                    if (i == 0) {
                        talentObj.total += consume[i].level * consumeObj.consume;
                    } else {
                        talentObj.total += (consume[i].level - consume[i - 1].level) * consumeObj.consume;
                    }
                }
            }
        }
    }


    //是否有后置条件
    function isHasSuffixCondition(talentClass, shortName, conditionShortName, consume) {
        var returnBool = false;
        var talentInfo = talentConfig[talentClass].talent;
        var t = talentInfo[shortName];
        if (t.conditions != null && t.conditions.length >= 0) {
            for (var i = 0; i < t.conditions.length; i++) {
                var condit = t.conditions[i];
                //判断后置
                if (condit['condition'] == conditionShortName && talentObj.addTalent[shortName] != null && talentObj.addTalent[shortName] > 0 && talentObj.addTalent[conditionShortName] - 1 < condit['number']) {
                    returnBool = true;
                } else if (condit['condition'] == 'all' && talentObj.addTalent[shortName] != null && talentObj.addTalent[shortName] > 0) {
                    //判断减点元素不是该后置元素，如果减点元素中条件是all
                    // 判断该元素是否已经加点，将加的点数从总点数中去除，判断剩余的总点是否满足条件
                    var tNo = parseInt(shortName.substr(1, shortName.length - 1));
                    var currentTNo = parseInt(conditionShortName.substr(1, conditionShortName.length - 1));
                    if (currentTNo < tNo) {
                        var addTotal = 0;
                        for (var tidx = 0; tidx < tNo; tidx++) {
                            if (talentObj.addTalent['t' + tidx] != null) {
                                addTotal += calConsumeTalent(talentClass, 't' + tidx);
                            }
                        }
                        if (addTotal - consume < condit['number']) {
                            returnBool = true;
                            break;
                        }
                    }
                }
            }
        }

        return returnBool;
    }

    function calConsumeTalent(tclass, shortName) {
        var returnInt = 0;

        var talentInfo = talentConfig[tclass].talent[shortName];
        var consume = talentInfo['consume'];

        for (var i = 0; i < consume.length; i++) {
            var consumeObj = consume[i];
            if (talentObj.addTalent[shortName] <= consumeObj.level) {
                if (i == 0) {
                    returnInt += talentObj.addTalent[shortName] * consumeObj.consume;
                } else {
                    returnInt += (talentObj.addTalent[shortName] - consume[i - 1].level) * consumeObj.consume;
                }
                break;
            } else {
                if (i == 0) {
                    returnInt += consume[i].level * consumeObj.consume;
                } else {
                    returnInt += (consume[i].level - consume[i - 1].level) * consumeObj.consume;
                }
            }
        }
        return returnInt;
    }

    //满足前置条件
    function allowCondition(talentClass, shortName, hasAddCount) {
        var returnBool = true;
        var talentInfo = talentConfig[talentClass].talent;
        if (talentInfo[shortName].conditions == null || talentInfo[shortName].conditions.length == 0) {
            returnBool = true;
        } else {
            for (var i = 0; i < talentInfo[shortName].conditions.length; i++) {
                var condit = talentInfo[shortName].conditions[i];
                if (condit['condition'] == 'all' && hasAddCount >= condit['number']) {
                    returnBool = returnBool && true;
                } else if (talentObj.addTalent[condit['condition']] != null && talentObj.addTalent[condit['condition']] >= condit['number']) {
                    returnBool = returnBool && true;
                } else {
                    returnBool = returnBool && false;
                }
            }
        }
        return returnBool;
    }

    function initCondition(talentClass, shortName) {
        var returnBool = true;
        var talentInfo = talentConfig[talentClass].talent;
        if (talentObj.addTalent[shortName] <= 0) {
            return returnBool
        }
        if (talentInfo[shortName].conditions == null || talentInfo[shortName].conditions.length == 0) {
            returnBool = true;
        } else {
            for (var i = 0; i < talentInfo[shortName].conditions.length; i++) {
                var condit = talentInfo[shortName].conditions[i];
                if (condit['condition'] == 'all') {
                    returnBool = returnBool && true;
                } else if (talentObj.addTalent[condit['condition']] != null && talentObj.addTalent[condit['condition']] >= condit['number']) {
                    returnBool = returnBool && true;
                } else {
                    returnBool = returnBool && false;
                }
            }
        }
        return returnBool;
    }

